package Basic_Structure_and_Syntax;

public class datatypes {
     public static void main(String[] args) {
     System.out.print("Activity 2: Data Types\n");
         
     byte myByte = 100;
     System.out.println("myByte: " + myByte);
         
     short myShort = 5000;
     System.out.println("myShort: " + myShort);
         
     int myInt = 100000;
     System.out.println("myInt: " + myInt);
         
     long myLong = 15000000000L;
     System.out.println("myLong: " + myLong);

    float myfloat = 5.75f;
    System.out.println("myfloat: " + myfloat);

    double myDouble = 19.99d;
    System.out.println("myDouble: " + myDouble);

    float myScientificFloat = 35e3f;
    System.out.println("myScientificFloat: " + myScientificFloat);

    double myScientificDouble = 12E4d;
    System.out.println("myScientificDouble: " + myScientificDouble);

    boolean myBoolean = true;
    System.out.println("myBoolean: " + myBoolean);  

    char myChar = 'A';  
    System.out.println("myChar: " + myChar);

    char myUnicodeChar = '\u0041';
    System.out.println("myUnicodeChar: " + myUnicodeChar);

    char myVar = 35;
    char myVar2 = 36; 
    char myVar3 = 37;
    System.out.println("myVar: " + myVar);
    System.out.println("myVar2: " + myVar2);
    System.out.println("myVar3: " + myVar3);

    

     }
}



     
    
